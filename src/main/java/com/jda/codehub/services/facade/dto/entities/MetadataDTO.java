package com.jda.codehub.services.facade.dto.entities;

public class MetadataDTO {
	
	private String level;
	private Integer rating;
	private Integer users_attempted;
	
	
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public Integer getRating() {
		return rating;
	}
	public void setRating(Integer rating) {
		this.rating = rating;
	}
	public Integer getUsers_attempted() {
		return users_attempted;
	}
	public void setUsers_attempted(Integer users_attempted) {
		this.users_attempted = users_attempted;
	}
	
	

}
